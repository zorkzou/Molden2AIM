!  451   452   453   454   455   456   457   458   459
!
!  The real spherical harmonics are defined in
!  http://en.wikipedia.org/wiki/Table_of_spherical_harmonics
!
   if(lq == 'S')then
     ncomp(ishell) = 1
     label(ibas + 1) = 001
   else if(lq == 'P')then
     ncomp(ishell) = 3
     label(ibas + 1) = 101
     label(ibas + 2) = 102
     label(ibas + 3) = 103
   else if(lq == 'D')then
