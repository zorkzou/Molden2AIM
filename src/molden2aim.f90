!%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
!
! v * S * v', S is a symmetric L.T. matrix
!
!%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
function VSV(N,v,s)
 implicit real(kind=8) (a-h,o-z)
 dimension         :: v(N),s(*)

 p=0.d0
 do i=1,N
   k=IdxNTT(1,i)-1
   do j=1,i-1
     k=k+1
     q=v(j)*s(k)*v(i)
     p=p+q+q
   end do
   k=k+1
   q=v(i)*s(k)*v(i)
   p=p+q
 end do
 VSV=p

 return
end

!%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
!
! Trace of the product of lower triangular matrices A and B.
!
!%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
function TracLL(N,A,B)
 implicit real(kind=8) (a-h,o-z)
 dimension         :: A(*), B(*)

 Su = 0.0d0
 Do I = 1, N
   II = (I*(I-1))/2
   Do J = 1, I-1
     Su = Su + (A(II+J)+A(II+J))*B(II+J)
   End Do
   Su = Su + A(II+I)*B(II+I)
 End Do

 TracLL = Su
 Return
End

!%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
!
! It returns the position of element (J,I) in a L.T. matrix.
! J must be .LE. I. It doesn't work if J > I.
!
!%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
function IdxNTT(J,I)
 implicit real(kind=8) (a-h,o-z)

 IdxNTT=J+(I-1)*I/2

 Return
End

