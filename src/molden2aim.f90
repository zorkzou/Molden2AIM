 nmo=0       ! number of occupied orbitals
 ene=0.d0
 occ=0.d0
 Isp=0       ! 0 (unknown), 1 (alpha or alpha+beta), 2 (beta)

 200  read(imo0,"(100a)",end=500)tmp
 if(len_trim(tmp) == 0) goto 200
 call charl2u(tmp)
 if(index(tmp,'ENE') /= 0)then
   ene=0.d0
   is=index(tmp,'=')
