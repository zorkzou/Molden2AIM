       label(ibas + 2) = 252
       label(ibas + 3) = 253
       label(ibas + 4) = 254
       label(ibas + 5) = 251
     end if
   else if(lq == 'F')then
     if(lsph == 0) then
       ncomp(ishell) = 10
       label(ibas + 1) = 301
       label(ibas + 2) = 307
       label(ibas + 3) = 310
       label(ibas + 4) = 304
       label(ibas + 5) = 302
       label(ibas + 6) = 303
       label(ibas + 7) = 306
       label(ibas + 8) = 309
       label(ibas + 9) = 308
       label(ibas +10) = 305
     else
       ncomp(ishell) = 7
       label(ibas + 1) = 351
       label(ibas + 2) = 352
       label(ibas + 3) = 353
       label(ibas + 4) = 354
       label(ibas + 5) = 355
       label(ibas + 6) = 356
       label(ibas + 7) = 357
     end if
   else if(lq == 'G')then
     if(lsph == 0) then
       ncomp(ishell) = 15
       label(ibas + 1) = 401
       label(ibas + 2) = 411
       label(ibas + 3) = 415
       label(ibas + 4) = 402
       label(ibas + 5) = 403
       label(ibas + 6) = 407
       label(ibas + 7) = 412
       label(ibas + 8) = 410
       label(ibas + 9) = 414
       label(ibas +10) = 404
       label(ibas +11) = 406
       label(ibas +12) = 413
       label(ibas +13) = 405
       label(ibas +14) = 408
       label(ibas +15) = 409
     else
       ncomp(ishell) = 9
       label(ibas + 1) = 451
       label(ibas + 2) = 452
       label(ibas + 3) = 453
       label(ibas + 4) = 454
       label(ibas + 5) = 455
       label(ibas + 6) = 456
       label(ibas + 7) = 457
       label(ibas + 8) = 458
       label(ibas + 9) = 459
     end if
   end if

   do i=1,ncomp(ishell)
     ibas = ibas + 1
     nc(ibas) = iatm
   end do
!  basis functions in igto have been normalized
   do i=1,ncon
     iexp = iexp + 1
     read(igto,*)expn(iexp),con(iexp)
   end do
 end do

 return
end

