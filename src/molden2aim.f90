   write(igto,"(i5,' 0')")i
   100  read(igtold,"(100a)",end=200)tmp
   if(tmp(1:1) == '&')goto 200
   write(igto,"(a)")trim(tmp)
   goto 100
   200  continue
 end do

 9999  return
end

