!
! This subroutine is taken from BLAS (version 3.4.0, November 2011).
! BLAS is a software package provided by Univ. of Tennessee, Univ. of California Berkeley, Univ. of Colorado Denver and NAG Ltd.
!
!%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
Subroutine DGEMM(TRANSA,TRANSB,M,N,K,ALPHA,A,LDA,B,LDB,BETA,C,LDC)
! Scalar Arguments ..
 DOUBLE PRECISION ALPHA,BETA
 INTEGER K,LDA,LDB,LDC,M,N
 CHARACTER TRANSA,TRANSB
! Array Arguments ..
 DOUBLE PRECISION A(LDA,*),B(LDB,*),C(LDC,*)
! Intrinsic Functions ..
 INTRINSIC MAX
! Local Scalars ..
 DOUBLE PRECISION TEMP
 INTEGER I,INFO,J,L,NCOLA,NROWA,NROWB
 LOGICAL NOTA,NOTB
! Parameters ..
 DOUBLE PRECISION ONE,ZERO
 PARAMETER (ONE=1.0D+0,ZERO=0.0D+0)

! Set  NOTA  and  NOTB  as  true if  A  and  B  respectively are not transposed and set  NROWA, NCOLA and  NROWB
!  as the number of rows and  columns of  A  and the  number of  rows  of  B  respectively.
 NOTA = TRANSA == 'N'
 NOTB = TRANSB == 'N'
 IF (NOTA) THEN
     NROWA = M
     NCOLA = K
 ELSE
     NROWA = K
     NCOLA = M
 END IF
 IF (NOTB) THEN
     NROWB = K
 ELSE
     NROWB = N
 END IF

! Test the input parameters.
 INFO = 0
 IF ((.NOT.NOTA) .AND. (.NOT.(TRANSA == 'C')) .AND. (.NOT.(TRANSA == 'T'))) THEN
     INFO = 1
 ELSE IF ((.NOT.NOTB) .AND. (.NOT.(TRANSB == 'C')) .AND. (.NOT.(TRANSB == 'T'))) THEN
     INFO = 2
 ELSE IF (M < 0) THEN
     INFO = 3
 ELSE IF (N < 0) THEN
     INFO = 4
 ELSE IF (K < 0) THEN
     INFO = 5
 ELSE IF (LDA < MAX(1,NROWA)) THEN
     INFO = 8
 ELSE IF (LDB < MAX(1,NROWB)) THEN
     INFO = 10
 ELSE IF (LDC < MAX(1,M)) THEN
     INFO = 13
 END IF
 IF (INFO /= 0) THEN
     WRITE( *, FMT = 9999 ) INFO
     call estop
 END IF

! Quick return if possible.
 IF ((M == 0) .OR. (N == 0) .OR. (((ALPHA == ZERO).OR. (K == 0)).AND. (BETA == ONE))) RETURN
! And if  alpha == zero.
 IF (ALPHA == ZERO) THEN
     IF (BETA == ZERO) THEN
         DO J = 1,N
             DO I = 1,M
                 C(I,J) = ZERO
             end do
         end do
     ELSE
         DO J = 1,N
             DO I = 1,M
                 C(I,J) = BETA*C(I,J)
             end do
         end do
     END IF
     RETURN
 END IF

! Start the operations.
 IF (NOTB) THEN
     IF (NOTA) THEN

!        Form  C := alpha*A*B + beta*C.
         DO J = 1,N
             IF (BETA == ZERO) THEN
                 DO I = 1,M
                     C(I,J) = ZERO
                 end do
             ELSE IF (BETA /= ONE) THEN
                 DO I = 1,M
                     C(I,J) = BETA*C(I,J)
                 end do
             END IF
             DO L = 1,K
                 IF (B(L,J) /= ZERO) THEN
                     TEMP = ALPHA*B(L,J)
                     DO I = 1,M
                         C(I,J) = C(I,J) + TEMP*A(I,L)
                     end do
                 END IF
             end do
         end do
     ELSE

!        Form  C := alpha*A**T*B + beta*C
         DO J = 1,N
             DO I = 1,M
                 TEMP = ZERO
                 DO L = 1,K
                     TEMP = TEMP + A(L,I)*B(L,J)
                 end do
                 IF (BETA == ZERO) THEN
                     C(I,J) = ALPHA*TEMP
                 ELSE
                     C(I,J) = ALPHA*TEMP + BETA*C(I,J)
                 END IF
             end do
         end do
     END IF
 ELSE
     IF (NOTA) THEN
!        Form  C := alpha*A*B**T + beta*C
         DO J = 1,N
             IF (BETA == ZERO) THEN
                 DO I = 1,M
                     C(I,J) = ZERO
                 end do
             ELSE IF (BETA /= ONE) THEN
                 DO I = 1,M
                     C(I,J) = BETA*C(I,J)
                 end do
             END IF
             DO L = 1,K
                 IF (B(J,L) /= ZERO) THEN
                     TEMP = ALPHA*B(J,L)
                     DO I = 1,M
                         C(I,J) = C(I,J) + TEMP*A(I,L)
                     end do
                 END IF
             end do
         end do
     ELSE
!        Form  C := alpha*A**T*B**T + beta*C
         DO J = 1,N
             DO I = 1,M
                 TEMP = ZERO
                 DO L = 1,K
                     TEMP = TEMP + A(L,I)*B(J,L)
                 end do
                 IF (BETA == ZERO) THEN
                     C(I,J) = ALPHA*TEMP
                 ELSE
                     C(I,J) = ALPHA*TEMP + BETA*C(I,J)
                 END IF
             end do
         end do
     END IF
 END IF

 RETURN
 9999 FORMAT( ' ** On entry to DGEMM parameter number ', I2, ' had an illegal value' )
END

