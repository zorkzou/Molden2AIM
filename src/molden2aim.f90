 return
end

!%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
!
! C = C + A * B, where A and B are symmetric in L.T. and C is in Sq. The following relationship is adopted:
! M_sq(j,i) = M_lt(j+(i-1)*i/2) if j <= i
!           = M_lt(i+(j-1)*j/2) if j > i
!
!%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
Subroutine LTxLT(N,A,B,C)
 implicit real(kind=8) (a-h,o-z)
 parameter(half=0.5d0)
 dimension         :: A(*),B(*),C(N,N)

 do i = 1,N
   i0 = i*(i-1)/2
   X = A(i+i0)
   do j = 1,i
     C(j,i) = X * B(j+i0)
   end do
   do j = i + 1,N
     j0 = j*(j-1)/2
     C(j,i) = X * B(i+j0)
   end do
   do j = 1,i - 1
     j0 = j*(j-1)/2
     X = A(j+i0)
     do k = 1,j
       C(k,i) = C(k,i) + X * B(k+j0)
     end do
     do k = j+1,N
       k0 = k*(k-1)/2
       C(k,i) = C(k,i) + X * B(j+k0)
     end do
   end do
   do j = i + 1,N
     j0 = j*(j-1)/2
     X = A(i+j0)
     do k = 1,j
       C(k,i) = C(k,i) + X * B(k+j0)
     end do
     do k = j+1,N
       k0 = k*(k-1)/2
       C(k,i) = C(k,i) + X * B(j+k0)
     end do
   end do
 end do

 return
end

!%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
!
! spin of MO:
!   0: unknown
!   1: Alpha, or Alpha and Beta
!   2: Beta
! NOTE: tmp must be in upper case and contains 'SPIN'
!
!%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
function MOspin(tmp)
 implicit real(kind=8) (a-h,o-z)
 character*100     :: tmp

 if(index(tmp,'ALPHA') /= 0)then
   MOspin=1
 else if(index(tmp,'BETA') /= 0)then
   MOspin=2
 else
   MOspin=0
 end if

 return
end

