   if(index(tmp(is+1:),'***') == 0) then
     read(tmp(is+1:),*) ene
     ene = sign(min(abs(ene), 1.d6), ene)
   end if
 else if(index(tmp,'OCCUP') /= 0)then
   is=index(tmp,'=')
   read(tmp(is+1:),*) occ
   nmotot=nmotot+1
   if(abs(occ) > th) nmo=nmo+1
   sumocc=sumocc+occ
 else if(index(tmp,'SPIN') /= 0)then
