!
! reorder, and dump igtold to igto
!   NOTE: because of some uncertainties in the MOLDEN format, atoms in {Atoms} and [GTO] must be ordered ascendingly.
!
!%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
